    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            mParam1 = getArguments().getString(ARG_PARAM1);
            mParam2 = getArguments().getString(ARG_PARAM2);
        }
    }

    private GridView gridview;
    private List<Home.DataEntity.ProductsEntity> data;
    private CategoryListAdapter adapter;

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        View view = inflater.inflate(R.layout.fragment_category, container, false);
        gridview = (GridView) view.findViewById(R.id.gv_category);
        data = new ArrayList<>();
        adapter = new CategoryListAdapter(getContext(), data);
        gridview.setAdapter(adapter);
        HttpUtil.requestGet(mParam1, new IRequestCallBack() {
            @Override
            public void onSuccess(String result) {
                Gson gson = new Gson();
                Home home = gson.fromJson(result, Home.class);
                data.addAll(home.getData().getProducts());
                adapter.notifyDataSetChanged();
            }
        });

        gridview.setOnItemClickListener(new AdapterView.OnItemClickListener() {
            @Override
            public void onItemClick(AdapterView<?> parent, View view, int position, long id) {
                String id1 = Http.DETAILS_PATH + data.get(position).getCurrent_sku().getId();
                Intent intent = new Intent(getActivity(),DetailsActivity.class);
                intent.putExtra("id",id1);
                startActivity(intent);
            }
        });
        return view;
    }

